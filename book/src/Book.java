public class Book {
    private final String title;
    private final int pages;

    // Constructor for Book class
    public Book(String title, int pages) {
        this.title = title;
        this.pages = pages;
    }

    // Getter for title
    public String getTitle() {
        return title;
    }

    // Getter for pages
    public int getPages() {
        return pages;
    }
}